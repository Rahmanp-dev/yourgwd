"use client";
import React, { useState } from 'react';
import { Database, Upload, CheckCircle, AlertTriangle } from 'lucide-react';

export default function AntigravitySync({ onRefreshLeads }) {
  const [jsonPayload, setJsonPayload] = useState('');
  const [syncing, setSyncing] = useState(false);
  const [result, setResult] = useState(null);

  const handleSync = async () => {
    setSyncing(true);
    setResult(null);

    try {
      let parsedData = JSON.parse(jsonPayload);
      
      // Auto-wrap single objects into arrays
      if (!Array.isArray(parsedData) && parsedData.name) {
        parsedData = [parsedData];
      } else if (parsedData.leads && Array.isArray(parsedData.leads)) {
        parsedData = parsedData.leads;
      }

      const token = localStorage.getItem('gwd_token');
      const res = await fetch('/api/sync/commit', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify({ leads: parsedData })
      });

      const data = await res.json();
      if (data.success) {
        setResult({ type: 'success', message: `Successfully synced! ${data.imported} new leads imported, ${data.updated} updated.` });
        setJsonPayload('');
        if (onRefreshLeads) onRefreshLeads();
      } else {
        setResult({ type: 'error', message: data.error || 'Server error during sync.' });
      }
    } catch (err) {
      setResult({ type: 'error', message: 'Invalid JSON format: ' + err.message });
    } finally {
      setSyncing(false);
    }
  };

  return (
    <div className="animate-slide-up" style={{ display: 'flex', flexDirection: 'column', gap: '32px' }}>
      <div className="glass-panel" style={{ padding: '32px' }}>
        <h2 style={{ fontSize: '1.5rem', marginBottom: '8px', display: 'flex', alignItems: 'center', gap: '10px' }}>
          <Database size={24} style={{ color: 'var(--primary)' }} /> Antigravity Data Sync
        </h2>
        <p style={{ color: 'var(--text-secondary)', fontSize: '0.95rem', marginBottom: '24px', lineHeight: 1.5 }}>
          Paste the JSON lead data generated by Antigravity's research and building phases. 
          This will securely push the leads, generated preview URLs, and drafted WhatsApp messages directly into your live MongoDB CRM.
        </p>

        {result && (
          <div style={{
            padding: '16px', borderRadius: '8px', marginBottom: '24px',
            background: result.type === 'success' ? 'rgba(16, 185, 129, 0.1)' : 'rgba(239, 68, 68, 0.1)',
            border: `1px solid ${result.type === 'success' ? 'rgba(16, 185, 129, 0.3)' : 'rgba(239, 68, 68, 0.3)'}`,
            color: result.type === 'success' ? '#34d399' : '#f87171',
            display: 'flex', alignItems: 'center', gap: '8px'
          }}>
            {result.type === 'success' ? <CheckCircle size={18} /> : <AlertTriangle size={18} />}
            {result.message}
          </div>
        )}

        <div style={{ display: 'flex', flexDirection: 'column', gap: '16px' }}>
          <textarea
            className="form-input"
            style={{ 
              minHeight: '300px', 
              fontFamily: 'monospace', 
              fontSize: '0.85rem', 
              background: '#040711', 
              color: '#38bdf8',
              resize: 'vertical'
            }}
            placeholder={'[\n  {\n    "name": "Acme Corp",\n    "city": "Austin",\n    "niche": "Plumber",\n    "score": 8.5,\n    "previewUrl": "/client/acme",\n    "whatsappMessage": "Hi there...",\n    "workDate": "2026-06-14"\n  }\n]'}
            value={jsonPayload}
            onChange={(e) => setJsonPayload(e.target.value)}
          ></textarea>

          <div style={{ display: 'flex', justifyContent: 'flex-end' }}>
            <button 
              className="btn-primary" 
              disabled={syncing || !jsonPayload.trim()} 
              onClick={handleSync}
            >
              <Upload size={18} />
              {syncing ? 'Syncing to Database...' : 'Commit to CRM Database'}
            </button>
          </div>
        </div>
      </div>
    </div>
  );
}
